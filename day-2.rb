# {horizontal: 0, depth: 0}
def find_submarine_position(puzzle_input)
  formatted_directions = puzzle_input.map { |direction| direction.split(" ")}
  horizontal_position = horizontal_position(formatted_directions)
  depth_position = depth_position(formatted_directions)
  horizontal_position * depth_position # puzzle answer
end

def get_forward_directions(formatted_directions_list)
  formatted_directions_list.select do |direction|
    direction[0] == "forward"
  end
end

def calculate_horizontal_position(forward_directions)
  forward_directions.sum { |direction| direction[1].to_i }
end

def horizontal_position(formatted_directions_list)
  forward_directions = get_forward_directions(formatted_directions_list)
  calculate_horizontal_position(forward_directions)
end

def depth_position(formatted_directions_list)
  up_directions = get_up_directions(formatted_directions_list)
  up_position = calculate_up_position(up_directions)
  down_directions = get_down_directions(formatted_directions_list)
  down_position = calculate_down_position(down_directions)
  (down_position - up_position).abs
end

def get_up_directions(formatted_directions_list)
  formatted_directions_list.select do |direction|
    direction[0] == "up"
  end
end

def get_down_directions(formatted_directions_list)
  formatted_directions_list.select do |direction|
    direction[0] == "down"
  end
end

def calculate_up_position(up_directions)
  up_directions.sum { |direction| direction[1].to_i }
end

def calculate_down_position(down_directions)
  down_directions.sum { |direction| direction[1].to_i }
end

